void sol()
{
    string s; cin >> s;
    
    string tmp, a="", b="";
    
    tmp = s;
    sort(tmp.begin(), tmp.end());
    
    a += tmp[0];
    
    bool flag = 0;
    
    for(ll i=0; i<s.length(); i++) {
        if(s[i] == a[0] && !flag) {
           flag = 1;
           continue;
        }
        
        b += s[i];
    }
    
    cout<<a<<' '<<b<<'\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll t;
    cin >> t;

    while (t--)
    {
        sol();
    }

    return 0;
}

