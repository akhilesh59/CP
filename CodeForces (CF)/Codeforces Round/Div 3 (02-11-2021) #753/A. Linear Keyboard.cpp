void sol()
{
    string s, x;
    cin >> s >> x;
    unordered_map<char,ll> mp;

    for(ll i=0; i<s.length(); i++) {
        mp[s[i]] = i;
    }

    ll ans = 0;
    for(ll i=0; i<x.length()-1; i++) {
        ans += abs(mp[x[i]] - mp[x[i+1]]);
    }

    cout<<ans<<'\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll t;
    cin >> t;

    while (t--)
    {
        sol();
    }

    return 0;
}

