void sol()
{
    ll n;
    cin >> n;
    vl v(n);
    for(auto &i : v) cin >> i;

    if(n == 1) {
        cout<<v[0]<<'\n';
        return;
    }

    sort_asc(v);
    ll i = 1;
    ll cum = v[0];
    ll maxi = v[0];

    for( ; i<n-1; i++) {
        v[i] -= cum;
        maxi = max(maxi,v[i]);
        cum += v[i];
    }

    v[n-1] = v[n-1] - cum;
    ll ans = max(maxi, v[n-1]);

    cout<<ans<<'\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll t;
    cin >> t;

    while (t--)
    {
        sol();
    }

    return 0;
}

