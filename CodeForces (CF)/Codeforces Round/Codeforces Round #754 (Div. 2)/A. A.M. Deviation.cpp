ll am(ll a, ll b, ll c) {
    return ((a+c) - 2*(b));
}

void sol()
{
    ll a,b,c;
    cin >> a >> b >> c;

    ll d = am(a,b,c);
    if(d == 0 || (abs(d)%3 == 0)) {
        cout<<"0\n";
        return;
    }
    else {
        ll ans = abs(d)%3;
        ans = min(ans, (3 - ans));
        cout<<ans<<'\n';
    }
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll t;
    cin >> t;

    while (t--)
    {
        sol();
    }

    return 0;
}

