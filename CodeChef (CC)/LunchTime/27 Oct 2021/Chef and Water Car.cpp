void sol()
{
    ll n,v;
    cin >> n >> v;

    ll mini=0,maxi=0;

    if(v >= n-1) mini = n-1;
    else {
        mini = ((n-v)*(n-v+1))/2 + v-1;
    }

    maxi = (n*(n-1))/2;

    cout<<maxi<<' '<<mini<<'\n';
}

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    ll t;
    cin >> t;

    while (t--)
    {
        sol();
    }

    return 0;
}

