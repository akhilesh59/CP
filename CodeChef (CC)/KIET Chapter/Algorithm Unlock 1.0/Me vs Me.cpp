void sol()
{
    string x1,x2,y1,y2;
    cin >> x1 >> x2 >> y1 >> y2;

    ll cnt1 = 0, cnt2 = 0, rem1,rem2;
    string s1 = "Soumy-Sigma", s2 = "Soumy-Beta";
    string ans;

    if(x1.length() > x2.length()) {
        ans = s1;
    }
    else if(x1.length() < x2.length()) ans = s2;
    
    else {
        for(ll i=0; i<x1.length(); i++) {
            if(x1[i] > x2[i]) {
                ans = s1;
                break;
            }
            else if(x1[i] < x2[i]) {
                ans = s2;
                break;
            }
        }
    }

    if(ans.length() == 0) ans = s2;

    cout<<ans<<'\n';

}

